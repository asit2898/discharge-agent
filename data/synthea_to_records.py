"""
Synthea -> Discrepancy-Detector dataset.

Takes REAL Synthea FHIR-R4 patient bundles (generated by synthea/), and for each
one: selects a real ambulatory index encounter, reconciles the patient's real
active meds / allergies / problems / labs, then plants ONE labeled clinical
discrepancy (or leaves a clean control) using the injector library from
generate_dataset.py.

Grounding is REAL wherever the real chart supports it — a real eGFR of 24, a real
aspirin allergy, a real active statin — and only synthesized when the chart lacks
the needed substrate. Every record records per-item provenance (real vs injected).

Output = Abridge's exact 8-field schema + a separate labels sidecar. Reproducible.
"""
