def load_bundle(path):
    b=json.load(open(path))
    res=[e["resource"] for e in b.get("entry",[])]
    return res

def latest_obs(res, code, as_of=None):
    hits=[o for o in res if o["resourceType"]=="Observation"
          and any(c.get("code")==code for c in o.get("code",{}).get("coding",[]))
          and o.get("valueQuantity")]
    if as_of: hits=[o for o in hits if (o.get("effectiveDateTime","") or "")[:10] <= as_of]
    if not hits: return None
    hits.sort(key=lambda o:o.get("effectiveDateTime",""))
    return hits[-1]

def amb_encounters(res):
    encs=[r for r in res if r["resourceType"]=="Encounter" and r.get("class",{}).get("code")=="AMB"
          and r.get("period",{}).get("start")]
    encs.sort(key=lambda e:e["period"]["start"])
    return encs

def pick_index_encounter(res):
    encs=amb_encounters(res)
    if not encs: encs=[r for r in res if r["resourceType"]=="Encounter"]
    if not encs: return None
    def score(e):
        t=(e.get("type",[{}])[0].get("text","") or "").lower()
        pref = 2 if "general examination" in t else (1 if "check up" in t else 0)
        return (pref, e.get("period",{}).get("start",""))
    return sorted(encs,key=score)[-1]

class SChart:
    """Bridges a real Synthea bundle into the shape the injectors expect.
       If target_enc is given, the chart reflects the patient state AS-OF that
       real encounter's date (for longitudinal series)."""
    def __init__(self, rng, path, res, target_enc=None):
        self.rng=rng; self.path=path; self.res=res
        self.patient=next(r for r in res if r["resourceType"]=="Patient")
        self.pid=self.patient["id"]
        self.gender=self.patient.get("gender","female")
        nm=self.patient["name"][0]
        self.given=clean_name((nm.get("given") or ["Alex"])[0])
        self.family=clean_name(nm.get("family","Doe"))
        self.birth=dt.date.fromisoformat(self.patient["birthDate"])
        self.name_display=f"{('Mr.' if self.gender=='male' else 'Ms.')} {self.given} {self.family}"
        self.all_amb=amb_encounters(res)
        self.index=target_enc or pick_index_encounter(res)
        self.enc_id=self.index["id"] if self.index else self.pid
        self.when_dt=self.index.get("period",{}).get("start","")[:10] if self.index else "2025-01-01"
        yr=int(self.when_dt[:4]); self.age=yr-self.birth.year
        self.archetype="synthea"
        aod=self.when_dt
        # real active meds AS-OF this visit -> our keys (dedup, keep resource for evidence passthrough)
        self.real_med_res=[m for m in res if m["resourceType"]=="MedicationRequest" and m.get("status")=="active"
                           and (m.get("authoredOn","") or "")[:10] <= aod]
        self.home_meds=[]
        for m in self.real_med_res:
            k=map_med(m.get("medicationCodeableConcept",{}).get("text"))
            if k and k not in self.home_meds: self.home_meds.append(k)
        # real active conditions AS-OF -> our labels + keep display list
        self.real_cond_res=[c for c in res if c["resourceType"]=="Condition"
                            and c.get("clinicalStatus",{}).get("coding",[{}])[0].get("code")=="active"
                            and ((c.get("onsetDateTime") or c.get("recordedDate") or "")[:10] <= aod)]
        self.conditions=[]; self.cond_displays=[]
        for c in self.real_cond_res:
            disp=c["code"]["coding"][0].get("display","")
            self.cond_displays.append(disp)
            lab=map_condition(disp)
            if lab and lab not in self.conditions: self.conditions.append(lab)
        # real allergies (chronic; include those recorded before the visit)
        self.real_allergy_res=[a for a in res if a["resourceType"]=="AllergyIntolerance"
                               and ((a.get("recordedDate","") or "")[:10] <= aod or not a.get("recordedDate"))]
        self.allergy=None; self.real_allergy_label=None
        for a in self.real_allergy_res:
            lab=map_allergy(a.get("code",{}).get("text") or a["code"]["coding"][0].get("display",""))
            if lab: self.allergy=lab; self.real_allergy_label=lab; break
        # labs AS-OF
        e=latest_obs(res,"33914-3",aod); self.egfr = round(e["valueQuantity"]["value"]) if e else None
        self.egfr_res=e
        self.a1c_res=latest_obs(res,"4548-4",aod); self.k_res=latest_obs(res,"6298-4",aod)

