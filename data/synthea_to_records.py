def mk_service_request(rng, pid, enc, specialty, when):
    return {"resourceType":"ServiceRequest","id":G.uid(rng),"status":"active","intent":"order",
            "code":{"text":f"Referral to {specialty}"},"subject":{"reference":G.urn(pid)},
            "encounter":{"reference":G.urn(enc)},"authoredOn":G.iso(when)}

def choose_window(encs, L):
    if len(encs)<L: return None
    tail=encs[-min(len(encs),8):]
    if len(tail)<L: tail=encs[-L:]
    idxs=sorted(set(round(k*(len(tail)-1)/(L-1)) for k in range(L)))
    seen=set(); win=[]
    for i in idxs:
        d=tail[i]["period"]["start"][:10]
        if d in seen: continue
        seen.add(d); win.append(tail[i])
    return win if len(win)>=2 else None

def _trend_label(analyte, series, injs, last, setup):
    vals=", ".join(f"{v:g}" for _,v in series)
    matchd="glomerular" if analyte=="eGFR" else "hemoglobin a1c"
    ev=G.line("DR", f"Your {analyte} has gone {vals} across these visits — we'll hold the current plan.")
    li=injs[last.enc_id]
    li["hpi_turns"]=[ev]
    li["note_plan_items"]=[(f"{analyte} trend","Values reviewed; regimen unchanged today.")]
    li["avs_next"]=["No changes to your medications today."]
    tl={"type":"worsening_trend","severity":"high" if analyte=="eGFR" else "moderate","analyte":analyte,
        "series":[{"date":d,"value":v} for d,v in series],
        "explanation":f"{analyte} is trending the wrong way across visits ({vals}) yet the plan is unchanged, with no escalation or workup.",
        "transcript_evidence":ev,"chart_evidence":{"resource_type":"Observation","match_display":matchd},
        "suggested_fix":f"Do not leave the worsening {analyte} unaddressed — escalate therapy/workup and document the trend."}
    return tl, {"trend":"real","analyte":analyte}

def _fallback_label(pick, charts, injs, last, setup, rng):
    if pick=="med_discontinued":
        important={"atorvastatin","simvastatin","lisinopril","enalapril","metoprolol","insulin glargine",
                   "levothyroxine","warfarin","metformin","amlodipine"}
        med=next((m for m in setup.home_meds if m in important), (setup.home_meds[0] if setup.home_meds else None))
        if med:
            li=injs[last.enc_id]
            stopped=G.mk_med(G.uid(rng),last.pid,last.enc_id,med,dt.date.fromisoformat(setup.when_dt),status="stopped")
            li["extra_resources"]=[("MedicationRequest",stopped)]; li["_drop_active_med"]=med
            ev=G.line("PT", f"I realized I'm not on the {med} anymore — I don't think anyone told me to stop it.")
            li["hpi_turns"]=[ev]; li["note_plan_items"]=[("Medication reconciliation","Reviewed current medications.")]
            tl={"type":"med_discontinued_without_reason","severity":"high" if med in ("warfarin","insulin glargine") else "moderate",
                "drug":med,"prior_visit":setup.when_dt,
                "explanation":f"{med} was active at the {setup.when_dt} visit but is absent from today's reconciled list with no documented reason for stopping.",
                "transcript_evidence":ev,"chart_evidence":{"resource_type":"MedicationRequest","match_display":med},
                "suggested_fix":f"Reconcile {med}: confirm whether the stop was intentional; restart or document the rationale."}
            return tl, {"home_med":"real"}
        pick="dropped_result"
    if pick=="dropped_referral":
        specialty=rng.choice(["nephrology","cardiology","endocrinology"])
        when_setup=dt.date.fromisoformat(setup.when_dt)
        si=injs[setup.enc_id]
        si["extra_resources"]=[("ServiceRequest",mk_service_request(rng,setup.pid,setup.enc_id,specialty,when_setup))]
        si["plan_turns"]=[G.line("DR",f"I'm putting in a referral to {specialty} for you today."),G.line("PT","Okay, thanks.")]
        si["note_plan_items"]=[("Referral",f"Referral to {specialty} placed.")]
        si["avs_next"]=[f"See {specialty} as referred."]
        li=injs[last.enc_id]
        li["extra_resources"]=[("ServiceRequest",mk_service_request(rng,last.pid,setup.enc_id,specialty,when_setup))]
        ev=G.line("PT", f"I still haven't managed to get in to see {specialty}, by the way.")
        li["hpi_turns"]=[ev, G.line("DR","Let's move on — how's everything else?")]
        li["note_plan_items"]=[("Interval history","No acute issues reported.")]
        tl={"type":"dropped_referral","severity":"moderate","specialty":specialty,"prior_visit":setup.when_dt,
            "explanation":f"A referral to {specialty} was placed at the {setup.when_dt} visit; the patient still hasn't been seen and today's plan does not follow up on it.",
            "transcript_evidence":ev,"chart_evidence":{"resource_type":"ServiceRequest","match_display":f"referral to {specialty}"},
            "suggested_fix":f"Close the loop on the {specialty} referral: verify scheduling, re-refer if needed, and document."}
        return tl, {"referral":"injected"}
    # dropped_result
    if "Type 2 diabetes mellitus" in setup.conditions: analyte,val,phrase,key,matchd=("A1c",9.8,"an A1c of 9.8%","a1c","hemoglobin a1c")
    else: analyte,val,phrase,key,matchd=("potassium",5.8,"a high potassium of 5.8","k","potassium")
    si=injs[setup.enc_id]
    si["labs"]={key:val}
    si["plan_turns"]=[G.line("DR",f"Your {analyte} came back high at {val} — let's recheck it next time."),G.line("PT","Sure.")]
    si["note_plan_items"]=[("Abnormal result",f"{analyte} {val}; recheck at next visit.")]
    li=injs[last.enc_id]
    days=max((dt.date.fromisoformat(last.when_dt)-dt.date.fromisoformat(setup.when_dt)).days,1)
    li["labs"]={key:val}; li["prior_lab_days"]=days
    ev=G.line("PT", f"Did that {analyte} from last time ever get rechecked? I never heard back.")
    li["hpi_turns"]=[ev, G.line("DR","We'll set that aside for today.")]
    li["note_plan_items"]=[("Follow-up","Continue current plan.")]
    tl={"type":"dropped_result","severity":"high" if key=="k" else "moderate","prior_visit":setup.when_dt,
        "explanation":f"An abnormal {phrase} at the {setup.when_dt} visit was flagged for recheck but is never followed up today.",
        "transcript_evidence":ev,"chart_evidence":{"resource_type":"Observation","match_display":matchd},
        "suggested_fix":f"Address the deferred {phrase}: repeat and act on it now; do not carry it forward unresolved."}
    return tl, {"prior_result":"injected"}

def _point_in_time(sc):
    """apply a point-in-time injector to a single visit (earlier visits in a series
       get their own catches). Prefer types the REAL chart supports; drug_allergy is
       last since it can always inject a synthetic allergy (keeps grounding real and
       the distribution balanced)."""
    med=["drug_drug_interaction","dropped_home_med","adverse_drug_event","duplicate_therapy","renal_dose_conflict"]
    sc.rng.shuffle(med)
    # medication-based real catches first; history/allergy only when no med catch applies
    for t in med + ["history_mismatch","drug_allergy_conflict"]:
        inj=G.INJECTORS[t](sc, sc.rng)
        if inj is not None: return inj
    return None

def longitudinal_patient(f, res, rng, variant):
    encs=amb_encounters(res)
    if len(encs)<4: return None
    L=rng.choice([2,3,3,4])
    window=choose_window(encs,L)
    if not window or len(window)<2: return None
    charts=[SChart(random.Random(sh(f+e["id"])), f, res, target_enc=e) for e in window]
    injs={}
    for c in charts:
        b=G._blank()
        b["note_plan_items"]=[("Chronic disease management","Stable; continue current regimen.")]
        b["plan_turns"]=[G.line("DR","Let's keep things as they are for now."),G.line("PT","Okay.")]
        b["avs_next"]=["Continue your current medications.","Return as scheduled."]
        injs[c.enc_id]=b
    setup=charts[0]; last=charts[-1]
    egfr_series=[(c.when_dt,c.egfr) for c in charts if c.egfr is not None]
    a1c_series=[(c.when_dt,c.a1c_res["valueQuantity"]["value"]) for c in charts if c.a1c_res]
    tl=grounding=None
    if len(egfr_series)>=3 and (egfr_series[0][1]-egfr_series[-1][1])>=8 and egfr_series[0][1]>egfr_series[-1][1]>0:
        tl,grounding=_trend_label("eGFR",egfr_series,injs,last,setup)
    elif len(a1c_series)>=3 and (a1c_series[-1][1]-a1c_series[0][1])>=1.0:
        tl,grounding=_trend_label("A1c",a1c_series,injs,last,setup)
    if tl is None:
        pick=["med_discontinued","dropped_referral","dropped_result","worsening_forced"][variant%4]
        if pick=="worsening_forced" and len(egfr_series)>=2:   # rotate a worsening case in even if mild
            tl,grounding=_trend_label("eGFR",egfr_series,injs,last,setup)
        else:
            if pick=="worsening_forced": pick="dropped_result"
            tl,grounding=_fallback_label(pick,charts,injs,last,setup,rng)
    injs[last.enc_id]["discrepancy_label"]=tl; injs[last.enc_id]["type"]=tl["type"]
    # earlier, non-reserved visits carry their own point-in-time catch
    reserved={last.enc_id}
    if tl["type"] in ("dropped_result","dropped_referral"): reserved.add(setup.enc_id)
    for c in charts:
        if c.enc_id in reserved: continue
        pit=_point_in_time(c)
        if pit is not None: injs[c.enc_id]=pit
    recs=[]; labs=[]
    for i,c in enumerate(charts):
        rec,lab=emit(c, injs[c.enc_id])
        lab["series"]={"visit_index":i,"n_visits":len(charts),"date":c.when_dt,
                       "prior_encounter_id": charts[i-1].enc_id if i>0 else None}
        if c.enc_id==last.enc_id:
            lab["temporal"]=True; lab["prior_encounter_id"]=setup.enc_id
            if lab["discrepancies"]: lab["discrepancies"][0]["grounding"]=grounding
        recs.append(rec); labs.append(lab)
    return recs,labs

def main():
    rng=random.Random(SEED)
    def load_pool(d):
        out=[]
        for f in sorted(glob.glob(os.path.join(d,"*.json"))):
            if any(x in f for x in ["hospitalInformation","practitionerInformation"]): continue
            res=load_bundle(f)
            if not any(r["resourceType"]=="Patient" for r in res): continue
            if not any(r["resourceType"]=="Encounter" for r in res): continue
            out.append((f,res))
        return out
    main_pool=load_pool(POOL_MAIN)
    preg_pool=load_pool(POOL_PREG)
    rng.shuffle(main_pool); rng.shuffle(preg_pool)
    used=set()

    # ---------- LONGITUDINAL SLICE: ~25 patients as 2-4 real-visit series ----------
    L_recs=[]; L_labs=[]; variant=0; n_series=0
    for f,res in main_pool:
        if len(L_recs)>=64 or n_series>=26: break
        if f in used or len(amb_encounters(res))<4: continue
        out=longitudinal_patient(f,res,rng,variant)
        if not out or len(out[0])<2: continue
        used.add(f); L_recs+=out[0]; L_labs+=out[1]; variant+=1; n_series+=1

    # ---------- SINGLE-VISIT POINT-IN-TIME FILL (to 100) ----------
    def take(disc_type):
        pool = preg_pool if disc_type=="drug_condition_conflict" else main_pool
        injector=G.INJECTORS["control" if disc_type=="control" else disc_type]
        for require_real in (True, False):
            for f,res in pool:
                if f in used: continue
                sc=SChart(random.Random(sh(f)), f, res)
                if disc_type=="drug_condition_conflict" and "Pregnancy" not in sc.conditions: continue
                if require_real and not real_pref(sc, disc_type): continue
                injected=injector(sc, sc.rng)
                if injected is None: continue
                rec,lab=emit(sc, injected)
                used.add(f); return rec,lab
        return None
    remaining=N_RECORDS-len(L_recs)
    weights={"control":20,"drug_allergy_conflict":13,"drug_drug_interaction":12,"renal_dose_conflict":11,
             "duplicate_therapy":9,"dropped_home_med":11,"history_mismatch":9,"drug_condition_conflict":5,
             "adverse_drug_event":11}
    bag=[t for t,w in weights.items() for _ in range(w)]; rng.shuffle(bag)
    S_recs=[]; S_labs=[]; i=0
    while len(S_recs)<remaining and i<len(bag)*6:
        got=take(bag[i%len(bag)]) or take("control"); i+=1
        if got: S_recs.append(got[0]); S_labs.append(got[1])

    records=(L_recs+S_recs)[:N_RECORDS]; labels=(L_labs+S_labs)[:N_RECORDS]
    n_long=len(L_recs)

    # ---- write ----
    base=os.path.join(OUT_DIR,"discrepancy-train")
    with open(base+".jsonl","w") as fo:
        for r in records: fo.write(json.dumps(r)+"\n")
    with open(base+".json","w") as fo: json.dump(records,fo,indent=1)
    with open(base+".labels.jsonl","w") as fo:
        for l in labels: fo.write(json.dumps(l)+"\n")

    from collections import Counter
    dc=Counter(); gr=Counter()
    TEMPORAL={"worsening_trend","dropped_result","dropped_referral","med_discontinued_without_reason"}
    n_temporal=0
    for l in labels:
        if l["is_control"] and not l["has_adverse_event"]: dc["control"]+=1
        for d in l["discrepancies"]:
            dc[d["type"]]+=1
            if d["type"] in TEMPORAL: n_temporal+=1
            for k,v in d.get("grounding",{}).items(): gr[f"{k}:{v}"]+=1
        for a in l["adverse_events"]: dc["adverse_drug_event"]+=1
    uniq=len({r["metadata"]["patient_id"] for r in records})
    multi=len([1 for l in labels if l.get("series",{}).get("n_visits",1)>1])
    summary={"name":"discrepancy-train","records":len(records),"unique_patients":uniq,
             "synthetic":True,"source":"synthea-fhir-r4 (California, US-Core IG) + discrepancy injection",
             "longitudinal":{"records_in_a_visit_series":multi,"series_patients":n_series,
                             "temporal_discrepancies":n_temporal,
                             "point_in_time_records":len(records)-multi},
             "label_distribution":dict(dc),"grounding_provenance":dict(gr),
             "index":[{"id":r["id"],"date":r["metadata"]["date"][:10],"visit_title":r["metadata"]["visit_title"],
                       "visit_index":labels[i].get("series",{}).get("visit_index"),
                       "n_visits":labels[i].get("series",{}).get("n_visits",1),
                       "has_discrepancy":labels[i]["has_discrepancy"],"has_adverse_event":labels[i]["has_adverse_event"],
                       "primary_type":(labels[i]["discrepancies"][0]["type"] if labels[i]["discrepancies"]
                                       else ("adverse_drug_event" if labels[i]["has_adverse_event"] else "control"))}
                      for i,r in enumerate(records)]}
    with open(os.path.join(OUT_DIR,"summary.json"),"w") as fo: json.dump(summary,fo,indent=1)
    print(f"Wrote {len(records)} records ({uniq} unique patients; {multi} in {n_series} visit-series).")
    print("Label distribution:",dict(dc))
    print("Temporal (cross-visit) discrepancies:",n_temporal)
    print("Grounding provenance:",dict(gr))

if __name__=="__main__":
    main()
