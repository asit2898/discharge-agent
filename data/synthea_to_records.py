def real_pref(sc, disc_type):
    """True if this real chart already contains the substrate for disc_type."""
    if disc_type=="drug_allergy_conflict": return sc.real_allergy_label is not None
    if disc_type=="renal_dose_conflict":   return sc.egfr is not None and sc.egfr<30
    if disc_type=="drug_condition_conflict":return "Pregnancy" in sc.conditions
    if disc_type=="dropped_result":         return sc.egfr is not None or bool(sc.a1c_res) or "warfarin" in sc.home_meds
    if disc_type in ("drug_drug_interaction","duplicate_therapy","dropped_home_med","adverse_drug_event"):
        return len(sc.home_meds)>=1
    if disc_type=="history_mismatch":       return len(sc.conditions)>=1
    return True

