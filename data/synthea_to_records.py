def bp_from(res, enc_id):
    """pull systolic/diastolic/hr/weight from the index encounter (or latest)."""
    def comp(o,code):
        for c in o.get("component",[]):
            if any(cc.get("code")==code for cc in c.get("code",{}).get("coding",[])):
                return c.get("valueQuantity",{}).get("value")
    obs=[o for o in res if o["resourceType"]=="Observation"]
    def latest(pred):
        hs=[o for o in obs if pred(o)]; hs.sort(key=lambda o:o.get("effectiveDateTime","")); return hs[-1] if hs else None
    bp=latest(lambda o:any(c.get("code")=="85354-9" for c in o.get("code",{}).get("coding",[])))
    hr=latest(lambda o:any(c.get("code")=="8867-4" for c in o.get("code",{}).get("coding",[])))
    wt=latest(lambda o:any(c.get("code")=="29463-7" for c in o.get("code",{}).get("coding",[])))
    sbp=comp(bp,"8480-6") if bp else None; dbp=comp(bp,"8462-4") if bp else None
    return (round(sbp) if sbp else 128, round(dbp) if dbp else 78,
            round(hr["valueQuantity"]["value"]) if hr else 74,
            round(wt["valueQuantity"]["value"]) if wt else 80)

def emit(sc, injected):
    rng=sc.rng; res=sc.res; pid=sc.pid; enc=sc.enc_id
    when=dt.date.fromisoformat(sc.when_dt)
    grounding={}

    related={"Condition":[],"Observation":[],"MedicationRequest":[],"AllergyIntolerance":[],"Procedure":[]}
    # --- real active problems (cap the social-finding noise but keep clinical ones) ---
    for c in sc.real_cond_res:
        related["Condition"].append(c)
    # --- real active meds (reconciled at visit); a silently-discontinued med is omitted ---
    drop_med=injected.get("_drop_active_med")
    for m in sc.real_med_res:
        if drop_med and map_med(m.get("medicationCodeableConcept",{}).get("text"))==drop_med: continue
        related["MedicationRequest"].append(m)
    # --- real allergies ---
    for a in sc.real_allergy_res:
        related["AllergyIntolerance"].append(a)
    # --- real vitals + labs carried into the visit ---
    sbp,dbp,hr,wt=bp_from(res,enc)
    for key,val in [("sbp",sbp),("dbp",dbp),("hr",hr),("wt",wt)]:
        related["Observation"].append(G.mk_obs(G.uid(rng),pid,enc,key,val,when,category="vital-signs"))
    if sc.egfr_res: related["Observation"].append(sc.egfr_res); grounding["egfr"]="real"
    if sc.a1c_res:  related["Observation"].append(sc.a1c_res)
    if sc.k_res:    related["Observation"].append(sc.k_res)

    # ---- apply injector chart mutations, preferring real substrate ----
    # allergy
    if injected["ensure_allergy"]:
        lab=injected["ensure_allergy"]
        real_present = any(map_allergy(a.get("code",{}).get("text") or a["code"]["coding"][0].get("display","") )==lab
                           for a in sc.real_allergy_res)
        if real_present: grounding["allergy"]="real"
        else:
            related["AllergyIntolerance"].append(G.mk_allergy(G.uid(rng),pid,lab,when-dt.timedelta(days=900)))
            grounding["allergy"]="injected"
    # egfr override (only if injector forces a value the real chart doesn't already satisfy)
    if injected["set_egfr"] is not None:
        if sc.egfr is not None and sc.egfr==injected["set_egfr"]:
            grounding["egfr"]="real"
        else:
            related["Observation"].append(G.mk_obs(G.uid(rng),pid,enc,"egfr",injected["set_egfr"],when))
            grounding["egfr"]= grounding.get("egfr","injected") if grounding.get("egfr")=="real" else "injected"
    # ensure a specific home med exists (DDI partner)
    for drug in injected["ensure_home_med"]:
        if drug not in sc.home_meds:
            related["MedicationRequest"].append(G.mk_med(G.uid(rng),pid,enc,drug,when-dt.timedelta(days=600),status="active"))
            grounding.setdefault("home_med","injected")
        else:
            grounding.setdefault("home_med","real")
    # newly prescribed this visit (the action being flagged)
    for drug in injected["prescribe"]:
        related["MedicationRequest"].append(G.mk_med(G.uid(rng),pid,enc,drug,when,status="active"))
    # extra labs the injector wants (e.g. dropped_result abnormal prior value)
    for key,val in injected["labs"].items():
        if key=="egfr" and grounding.get("egfr"): continue
        when_lab=when - dt.timedelta(days=injected.get("prior_lab_days",0))
        related["Observation"].append(G.mk_obs(G.uid(rng),pid,enc,key,val,when_lab))
    # carried-forward / injected temporal resources (prior referral, stopped med, prior abnormal obs)
    for rtype,resource in injected.get("extra_resources",[]):
        related.setdefault(rtype,[]).append(resource)

    # default grounding tags for real-substrate injectors
    if injected["type"] in ("drug_drug_interaction","duplicate_therapy","dropped_home_med") and "home_med" not in grounding:
        grounding["home_med"]="real"
    if injected["type"]=="renal_dose_conflict" and sc.egfr is not None and sc.egfr<30 and not injected["set_egfr"]:
        grounding["egfr"]="real"
    if injected["adverse_event_label"]:
        grounding["ade_med"]="real" if injected["adverse_event_label"].get("suspected_drug") in sc.home_meds else "injected"

    # ---- resolve label evidence against related resources (semantic, not substring) ----
    def _disp(r):
        return (r.get("code",{}).get("text") or r.get("medicationCodeableConcept",{}).get("text")
                or r.get("code",{}).get("coding",[{}])[0].get("display",""))
    def resolve(item):
        if not item: return
        ce=item.get("chart_evidence")
        if not ce: return
        target=ce.get("match_display",""); tl=target.lower(); wt=ce["resource_type"]
        cands=related.get(wt,[]); pick=None
        for r in cands:
            d=_disp(r)
            if wt=="AllergyIntolerance" and (map_allergy(d)==target or tl in d.lower()): pick=r;break
            if wt=="Condition" and (map_condition(d)==target or tl in d.lower()): pick=r;break
            if wt=="MedicationRequest" and (map_med(d)==target or tl in d.lower()): pick=r;break
            if wt=="Observation" and (tl in d.lower() or tl in json.dumps(r).lower()): pick=r;break
        if pick is None:                      # last-resort raw substring over any field
            for r in cands:
                if tl and tl in json.dumps(r).lower(): pick=r;break
        if pick is not None:
            ce["resource_id"]=pick["id"]; ce["display"]=_disp(pick)
        ce.pop("match_display",None)
    resolve(injected["discrepancy_label"]); resolve(injected["adverse_event_label"])

    # ================= TRANSCRIPT / NOTE / AVS =================
    transcript,note,avs = s_assemble(sc, injected, sbp,dbp,hr,wt)

    # ================= longitudinal summary (real counts) =================
    from collections import Counter
    rc=Counter(r["resourceType"] for r in res)
    med_labels=sorted({m.get("medicationCodeableConcept",{}).get("text","") for m in sc.real_med_res} |
                      {f"{d} {G.DRUGS[d]['dose']}" for d in injected["prescribe"]})
    allergy_labels=sorted({(a.get("code",{}).get("text") or a["code"]["coding"][0].get("display","")) for a in sc.real_allergy_res}
                          | ({injected["ensure_allergy"]} if injected["ensure_allergy"] and grounding.get("allergy")=="injected" else set()))

    visit_title=f"Follow-up visit — {', '.join([c for c in sc.conditions[:2]] or ['adult primary care'])}"
    record={
      "id":f"{pid}::{enc}",
      "metadata":{
        "source":"synthea-fhir-r4+discrepancy-injection","synthetic":True,
        "patient_id":pid,"encounter_id":enc,"encounter_reference":G.urn(enc),
        "date":G.iso(when),"status":"finished",
        "visit_type":(sc.index.get("type",[{}])[0].get("text") if sc.index else "General examination of patient (procedure)"),
        "document_status":"current",
        "related_resource_counts":{k:len(v) for k,v in related.items() if v},
        "visit_title":visit_title,
      },
      "patient_context":{
        "patient":sc.patient,
        "longitudinal_summary":{
          "resource_counts":dict(rc),
          "condition_labels":sc.cond_displays,
          "medication_labels":med_labels,
          "allergy_labels":allergy_labels,
        },
      },
      "encounter_fhir":{
        "encounter":sc.index or G.mk_encounter(enc,pid,when,"General examination of patient (procedure)",sc.name_display),
        "related_resources":{k:v for k,v in related.items() if v},
      },
      "transcript":transcript,"note":note,"after_visit_summary":avs,
      "after_visit_summary_provenance":{"method":"template_extractive_v1",
        "source":"clinical_note_assessment_and_plan","review_status":"not_clinically_reviewed"},
    }
    discs=[injected["discrepancy_label"]] if injected["discrepancy_label"] else []
    ades=[injected["adverse_event_label"]] if injected["adverse_event_label"] else []
    for d in discs: d["grounding"]=grounding
    label={"id":record["id"],"source":"synthea","archetype":"synthea:"+("+".join(sc.conditions[:3]) or "well"),
           "has_discrepancy":bool(discs),"discrepancies":discs,
           "has_adverse_event":bool(ades),"adverse_events":ades,
           "is_control":injected["type"] is None,"grounding":grounding}
    return record,label

def s_assemble(sc, injected, sbp,dbp,hr,wt):
    L=G.line; nm=sc.given
    spoken_conditions=[re.sub(r"\s*\((disorder|finding|situation|morphologic abnormality)\)","",d)
                       for d in sc.cond_displays
                       if not any(w in d.lower() for w in ["employ","education","stress","social","housing",
                       "medication review","risk activity","transport","violence","labor force","income","received"])][:4]
    T=[]
    T.append(L("DR", sc.rng.choice([f"Hi {nm}, good to see you again.",f"{nm}, come on in — how have you been?"])))
    T.append(L("PT", sc.rng.choice(["Doing okay, thanks.","Good to see you, doctor."])))
    T.append(L("DR","Anything new since your last visit?"))
    T += (injected["hpi_turns"] or [L("PT","Nothing major, staying about the same.")])
    T += (injected["review_turns"] or [
        L("DR","Let me confirm your medications."),
        L("PT","Same as before — "+(", ".join(sc.home_meds) if sc.home_meds else "nothing regular")+"."),
        L("DR","That matches my list.")])
    T.append(L("NURSE",f"BP {sbp} over {dbp}, heart rate {hr}, weight {wt} kilos."))
    labbits=[]
    if sc.egfr is not None: labbits.append(f"kidney function eGFR {sc.egfr}")
    if sc.a1c_res: labbits.append(f"A1c {sc.a1c_res['valueQuantity']['value']:.1f} percent")
    if labbits: T.append(L("DR","Your labs — "+", ".join(labbits)+"."))
    T.append(L("DR","Let's talk about the plan."))
    T += (injected["plan_turns"] or [L("DR","No changes today — keep on as you are."),L("PT","Sounds good.")])
    T.append(L("DR","Any questions before you head out?"))
    T.append(L("PT","No, thank you."))
    transcript="\n".join(T)

    sex="man" if sc.gender=="male" else "woman"
    subj=[f"{nm} {sc.family} is a {sc.age}-year-old {sex} here for follow-up."]
    if spoken_conditions: subj.append("Active problems include "+", ".join(c.lower() for c in spoken_conditions)+".")
    for t in injected["hpi_turns"]+injected["review_turns"]:
        spk,txt=t.split(": ",1)
        if spk=="PT" and any(w in txt for w in ["ran out","new","cough","muscle","swelling","never heard","bloodwork"]):
            subj.append("Patient reports: "+txt)
    objective=(f"Vitals: BP {sbp}/{dbp} mmHg, HR {hr}/min, weight {wt} kg. Exam: no acute distress. "
               f"Labs: "+(f"eGFR {sc.egfr} mL/min/1.73m2" if sc.egfr is not None else "reviewed")+".")
    plan_items=injected["note_plan_items"] or [("Chronic disease management","Stable; continue current regimen.")]
    ap=["**Assessment and Plan:**",""]
    for h,b in plan_items: ap += [f"### {h}",b,""]
    note=(f"**Subjective:** {' '.join(subj)}\n\n**Objective:** {objective}\n\n"+"\n".join(ap)).strip()
    nexts=injected["avs_next"] or ["Continue current care.","Return as scheduled."]
    avs=("Visit summary\n\nWhat we discussed\n"+"\n".join("• "+h for h,_ in plan_items)
         +"\n\nNext steps\n"+"\n".join("• "+n for n in nexts))
    return transcript,note,avs

